export const getGalleryTemplatesByCategory = (category) => {
  if (category === 'All') {
    return galleryTemplates;
  }
  return galleryTemplates.filter((template) => template.category === category);
};

export const getGalleryCategories = () => {
  const categories = new Set(galleryTemplates.map((t) => t.category));
  return ['All', ...Array.from(categories).sort()];
};
